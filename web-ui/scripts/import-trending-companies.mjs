#!/usr/bin/env node
/**
 * scripts/import-trending-companies.mjs (v1.16.0)
 *
 * Verifies the 13 trending Greenhouse + Ashby companies documented
 * in `docs/portals-examples.md` (v1.14.0 block) by hitting each
 * board's public API and printing a paste-ready YAML block the user
 * can append to their PARENT `portals.yml::tracked_companies`.
 *
 * Why a script (not a parent-write):
 *   CLAUDE.md hard rule #1 — web-ui never writes to parent files
 *   except on explicit user actions through web routes. A CLI
 *   pipeline that the user runs themselves (and pastes the output)
 *   keeps that contract clean.
 *
 * Usage:
 *   node scripts/import-trending-companies.mjs                # verify + print
 *   node scripts/import-trending-companies.mjs --check-only   # just print pass/fail per slug
 *   node scripts/import-trending-companies.mjs --enabled=true # set enabled:true on each entry
 *   node scripts/import-trending-companies.mjs --json         # machine-readable status
 *
 * Verification: each candidate hits the same boards-api the in-process
 * scanner uses; a 200 with non-empty job array counts as "live".
 * 404 / 403 / empty job array → mark the entry as needing review and
 * stamp `enabled: false`.
 */

import { resolveAdapter, ALL_ADAPTERS } from '../server/lib/portals/registry.mjs';

const TRENDING = [
  // Greenhouse-hosted
  { name: 'Stripe',       careers_url: 'https://job-boards.greenhouse.io/stripe',       notes: 'Heavy Go + Ruby. Remote-friendly.' },
  { name: 'GitLab',       careers_url: 'https://job-boards.greenhouse.io/gitlab',       notes: 'Fully remote; Go + Ruby.' },
  { name: 'HashiCorp',    careers_url: 'https://job-boards.greenhouse.io/hashicorp',    notes: 'Go infra; remote.' },
  { name: 'Cloudflare',   careers_url: 'https://job-boards.greenhouse.io/cloudflare',   notes: 'Heavy Go usage.' },
  { name: 'Datadog',      careers_url: 'https://job-boards.greenhouse.io/datadog',      notes: 'Go-heavy. Remote EU options.' },
  { name: 'Hugging Face', careers_url: 'https://job-boards.greenhouse.io/huggingface',  notes: 'ML / Python; remote-friendly.' },
  // Ashby-hosted
  { name: 'Notion',       careers_url: 'https://jobs.ashbyhq.com/notion' },
  { name: 'Linear',       careers_url: 'https://jobs.ashbyhq.com/linear',       notes: 'Top-tier product; Remote US/EU.' },
  { name: 'PostHog',      careers_url: 'https://jobs.ashbyhq.com/posthog',      notes: 'Remote-first analytics.' },
  { name: 'Replicate',    careers_url: 'https://jobs.ashbyhq.com/replicate' },
  { name: 'Modal Labs',   careers_url: 'https://jobs.ashbyhq.com/modal' },
  { name: 'Fly.io',       careers_url: 'https://jobs.ashbyhq.com/fly' },
  { name: 'Render',       careers_url: 'https://jobs.ashbyhq.com/render' },
];

const args = new Set(process.argv.slice(2));
const FLAGS = {
  checkOnly: args.has('--check-only'),
  json:      args.has('--json'),
  enabled:   process.argv.find((a) => a.startsWith('--enabled='))?.split('=')[1] === 'true',
};

const TIMEOUT_MS = 8000;

async function probe(entry) {
  const match = resolveAdapter(entry);
  if (!match) {
    return { ...entry, status: 'no-adapter', live: false, jobs: 0 };
  }
  try {
    const ctrl = new AbortController();
    const timer = setTimeout(() => ctrl.abort(), TIMEOUT_MS);
    const jobs = await match.adapter.fetch(match.endpoint, { signal: ctrl.signal });
    clearTimeout(timer);
    return {
      ...entry,
      api: match.endpoint,
      scan_method: match.adapter.id,
      status: jobs.length > 0 ? 'live' : 'empty',
      live: jobs.length > 0,
      jobs: jobs.length,
    };
  } catch (e) {
    return {
      ...entry,
      api: match.endpoint,
      scan_method: match.adapter.id,
      status: e.name === 'AbortError' ? 'timeout' : (e.status ? `http-${e.status}` : 'error'),
      live: false,
      jobs: 0,
      error: e.message,
    };
  }
}

function emitYaml(entries) {
  const lines = ['# Trending companies — generated by scripts/import-trending-companies.mjs',
                 `# Verified on ${new Date().toISOString().slice(0, 10)}`,
                 `# Append to your career-ops/portals.yml under tracked_companies:`,
                 ''];
  for (const e of entries) {
    const liveOk = e.live;
    const enabledFlag = FLAGS.enabled && liveOk;
    lines.push(`  - name: ${e.name}`);
    lines.push(`    careers_url: ${e.careers_url}`);
    if (e.api) lines.push(`    api: ${e.api}`);
    if (e.scan_method) lines.push(`    scan_method: ${e.scan_method}`);
    if (e.notes) lines.push(`    notes: "${e.notes}"`);
    lines.push(`    enabled: ${enabledFlag}    # status: ${e.status} (${e.jobs} jobs at fetch time)`);
    lines.push('');
  }
  return lines.join('\n');
}

function emitStatusTable(entries) {
  const w = Math.max(...entries.map((e) => e.name.length), 12);
  console.log(`\nTrending companies (${entries.length} candidates)\n`);
  console.log(`  ${'Name'.padEnd(w)}  ${'ATS'.padEnd(18)}  ${'Status'.padEnd(10)}  Jobs`);
  console.log(`  ${'-'.repeat(w)}  ${'-'.repeat(18)}  ${'-'.repeat(10)}  ----`);
  for (const e of entries) {
    const flag = e.live ? '✓' : e.status === 'no-adapter' ? '?' : '✗';
    console.log(`  ${flag} ${e.name.padEnd(w - 2)}  ${(e.scan_method || 'unknown').padEnd(18)}  ${e.status.padEnd(10)}  ${e.jobs}`);
  }
  const live = entries.filter((e) => e.live).length;
  console.log(`\n${live} / ${entries.length} live boards.\n`);
}

async function main() {
  process.stderr.write(`Probing ${TRENDING.length} trending companies via ${ALL_ADAPTERS.length} ATS adapters…\n`);
  // Run probes in parallel — they're independent HTTPS calls.
  const entries = await Promise.all(TRENDING.map(probe));

  if (FLAGS.json) {
    console.log(JSON.stringify(entries, null, 2));
    return;
  }
  if (FLAGS.checkOnly) {
    emitStatusTable(entries);
    return;
  }
  emitStatusTable(entries);
  process.stderr.write('--- paste-ready YAML follows on stdout ---\n');
  console.log(emitYaml(entries));
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
